package security;

public class Badge {
        private int badgeId;
        private String holderName;

        public int getBadgeId() { return badgeId; }
        public String getHolderName() { return holderName; }

        void setBadgeId(int badgeId) { this.badgeId = badgeId; }
        void setHolderName(String holderName) { this.holderName = holderName; }
    }

